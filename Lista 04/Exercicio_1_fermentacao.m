function f = Exercicio_1_fermentacao(t,x,T)
        w1=13.1;
        w2=0.005;
        w3=30;
        w4=0.94;
        w5=1.71;
        w6=20;
        T=x(3,1);
        b1=w1*((1-w2*(T-w3)^2)/(1-w2*(25-w3)^2));
        b2=w4*((1-w2*(T-w3)^2)/(1-w2*(25-w3)^2));
        b3=w5*((1-w2*(T-w6)^2)/(1-w2*(25-w6)^2));
        f=zeros(3,1);
        f(1)=b1*x(1,1)-(b1/b2)*x(1,1)^2;
        f(2)=b3*x(1,1);
        f(3)=0;
end